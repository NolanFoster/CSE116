
public class intValue {
	
	int number1 = 0;
	int number2 = 0;
	

	public int multiply(int n){
		n = number1 * number2;
		return n;
	}
	
	public int add(int n){
		n = number1 + number2;
		return n;
	}
	
	public double pow(double n){
		n = Math.pow(number1, number2);
		return n;
	}
}
